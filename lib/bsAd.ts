const DAY_MS = 86_400_000;

/** Convert a BS date to AD. Returns [year, month, day] or null if year is out of range. */
export function bsToAd(year: number, month: number, day: number): [number, number, number] | null {
  const row = BS_DATA[year];
  if (!row) return null;
  const [ay, am, ad] = row.adStart;
  let days = 0;
  for (let i = 0; i < month - 1; i++) days += row.days[i];
  days += day - 1;
  const start = Date.UTC(ay, am - 1, ad);
  const r = new Date(start + days * DAY_MS);
  return [r.getUTCFullYear(), r.getUTCMonth() + 1, r.getUTCDate()];
}

/** Convert an AD date to BS. Returns [year, month, day] or null if it falls outside the table. */
export function adToBs(year: number, month: number, day: number): [number, number, number] | null {
  const target = Date.UTC(year, month - 1, day);
  for (const yyKey of Object.keys(BS_DATA)) {
    const yy = Number(yyKey);
    const row = BS_DATA[yy];
    const [ay, am, ad] = row.adStart;
    const start = Date.UTC(ay, am - 1, ad);
    const totalDays = row.days.reduce((a, b) => a + b, 0);
    const end = start + (totalDays - 1) * DAY_MS;
    if (target >= start && target <= end) {
      let diff = Math.round((target - start) / DAY_MS);
      let m = 0;
      while (diff >= row.days[m]) { diff -= row.days[m]; m++; }
      return [yy, m + 1, diff + 1];
    }
  }
  return null;
}

const NE_DIGITS = '०१२३४५६७८९';
export const toNeDigits = (s: string | number): string =>
  String(s).replace(/[0-9]/g, (d) => NE_DIGITS[+d]);

export const toEnDigits = (s: string): string =>
  s.replace(/[०-९]/g, (d) => String(NE_DIGITS.indexOf(d)));

/** Format a BS date string (YYYY/MM/DD) — converts to Devanagari digits if requested. */
export function fmtBs(y: number, m: number, d: number, lang: 'ne' | 'en'): string {
  const s = `${String(y).padStart(4, '0')}/${String(m).padStart(2, '0')}/${String(d).padStart(2, '0')}`;
  return lang === 'ne' ? toNeDigits(s) : s;
}
